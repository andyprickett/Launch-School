numbers = [1, 2, 3, 4, 5]
numbers.delete_at(1)

# this will delete the object from the array at index position 1
# (in this case, the number 2)

numbers = [1, 2, 3, 4, 5]
numbers.delete(1)

# this will find and delete the object with the value of 1 from the array.
# (in this case, happens to be the first object in the array)
